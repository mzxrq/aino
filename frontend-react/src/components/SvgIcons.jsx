// Styled wrapper for action buttons
export const ActionButton = ({ Icon, onClick, title, theme = "light", filled = false }) => {
  const isDark = theme === "dark";
  return (
    <button
      className={`action-svg-btn ${isDark ? 'dark' : 'light'}`}
      onClick={onClick}
      title={title}
      style={{
        color: isDark ? "#fff" : "#64748b"
      }}
    >
      {Icon && <Icon filled={filled} />}
    </button>
  );
};
